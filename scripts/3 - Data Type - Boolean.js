/*
    BOOLEAN
    Tipe data boolean adalah nilai yang menunjukan suatu kebenaran atau kesalahan
    Tipe data boolean hanya teridisi dari:
        true, yaitu artinya benar
        false, yaitu artinya salah

    Tipe data ini biasa digunakan untuk mengetahui apakah sebuah pernyattan itu benar atau salah
*/


// True(Benar)
console.log(true);
console.log(typeof true);

// False(Salah)
console.log(false);
console.log(typeof false);